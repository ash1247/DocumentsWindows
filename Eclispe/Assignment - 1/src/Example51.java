//Example 5.1
//program for mathematical operations
public class Example51 {
		public static void main(String[] args)
		{
			float a = 20.5F, b = 6.4F;
			System.out.printf(" a = %.1f\n", + a); 
			System.out.printf(" b = %.1f\n", + b); 
			System.out.println(" a+b = " +(a+b));
			System.out.println(" a+b = " +(a-b));
			System.out.println(" a+b = " +(a*b));
			System.out.println(" a+b = " +(a/b));
			System.out.println(" a+b = " +(a%b));
			
		}
		
}
