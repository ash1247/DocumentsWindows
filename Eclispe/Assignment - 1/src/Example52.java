//Program on relational operators
public class Example52 {
	public static void main(String args[] )
	
	{
		float a = 15.0F, b = 20.75F, c = 15.0F;
		System.out.printf(" a = %f\n", a);
		System.out.printf(" b = %f\n", b);
		System.out.printf(" c = %f\n", c);
		System.out.printf(" a < b is %s\n", (a < b ) );
		System.out.printf(" a > b is %s\n", (a > b) );
		System.out.printf(" a == c is %s\n", (a == c) );
		System.out.printf(" a <= c is %s\n", (a <= c) );
		System.out.printf(" a >= b is %s\n", (a >= b) );
		System.out.printf(" b != c is %s\n", (b != c) );
		System.out.printf(" b == a+c is %s\n", (b == a + c) );
	}

}
