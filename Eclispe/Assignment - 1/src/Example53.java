//Example 5.3
//program on use of +++m and +++m
public class Example53 {
	public static void main(String [] args)
	{
		int m = 10, n = 20;
		System.out.println(" m = " +m);
		System.out.println(" n = " +n);
		System.out.println(" ++m = " + ++m);
		System.out.println(" ++n = " + ++n);
		System.out.println(" m = " +m);
		System.out.println(" n = " +n);
	}

}
