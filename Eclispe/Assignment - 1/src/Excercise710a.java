//Exercise 7.10 (a)
//Right angled Number Triangle 
public class Excercise710a {
	public static void main(String [] args)
	{
		int i, j;
		for(i = 1; i <= 5; i++)
		{
		    for(j = 1; j <= i; j++)  
		    {          
		        System.out.print(i);
		        System.out.print(" ");
		    }
		    
		    System.out.print("\n");        
		}
	}

}
